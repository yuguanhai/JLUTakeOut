package com.jlu.takeout.service;

import com.jlu.takeout.dto.*;
import com.jlu.takeout.entity.Orders;
import com.jlu.takeout.result.PageResult;
import com.jlu.takeout.vo.OrderPaymentVO;
import com.jlu.takeout.vo.OrderStatisticsVO;
import com.jlu.takeout.vo.OrderSubmitVO;
import com.jlu.takeout.vo.OrderVO;
import org.springframework.stereotype.Service;

@Service
public interface OrderService {

    OrderSubmitVO submitDTO(OrdersSubmitDTO ordersSubmitDTO);
    /**
     * 订单支付
     * @param ordersPaymentDTO
     * @return
     */
    OrderPaymentVO payment(OrdersPaymentDTO ordersPaymentDTO) throws Exception;

    /**
     * 支付成功，修改订单状态
     * @param outTradeNo
     */
    void paySuccess(String outTradeNo);

    /**
     * 用户端订单分页查询
     * @param page
     * @param pageSize
     * @param status
     * @return
     */
    PageResult pageQuery4User(int page, int pageSize, Integer status);

    void cancelOrder(Long id)  throws Exception;
    /**
     * 查询订单详情
     * @param id
     * @return
     */
    OrderVO details(Long id);

    void repetition(Long id);

    PageResult conditionSearch(OrdersPageQueryDTO ordersPageQueryDTO);
    /**
     * 各个状态的订单数量统计
     * @return
     */
    OrderStatisticsVO statistics();

    /**
     * 接单
     *
     * @param ordersConfirmDTO
     */
    void confirm(OrdersConfirmDTO ordersConfirmDTO);

    /**
     * 拒单
     *
     * @param ordersRejectionDTO
     */
    void rejection(OrdersRejectionDTO ordersRejectionDTO) throws Exception;

    /**
     * 商家取消订单
     *
     * @param ordersCancelDTO
     */
    void cancel(OrdersCancelDTO ordersCancelDTO) throws Exception;

    /**
     * 派送订单
     *
     * @param id
     */
    void delivery(Long id);

    /**
     * 完成订单
     *
     * @param id
     */
    void complete(Long id);

    void reminder(Long id);

    void updateRelate(Orders orders);
}
